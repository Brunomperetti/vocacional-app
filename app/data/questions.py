def get_all_questions() -> list[dict[str, str]]:
    """Devuelve todas las preguntas del banco inicial."""
    return TEST_QUESTIONS


def get_questions_by_dimension(dimension: str) -> list[dict[str, str]]:
    """Devuelve las preguntas que pertenecen a una dimensión RIASEC."""
    normalized_dimension = dimension.upper()
    return [
        question
        for question in TEST_QUESTIONS
        if question["dimension"] == normalized_dimension
    ]


def get_questions_by_block(block: str) -> list[dict[str, str]]:
    """Devuelve las preguntas que pertenecen a un bloque del test."""
    normalized_block = block.lower()
    return [
        question
        for question in TEST_QUESTIONS
        if question["block"].lower() == normalized_block
    ]


def get_question_by_id(question_id: str) -> dict[str, str] | None:
    """Devuelve una pregunta por ID o None si no existe."""
    normalized_id = question_id.upper()
    return next(
        (question for question in TEST_QUESTIONS if question["id"] == normalized_id),
        None,
    )
